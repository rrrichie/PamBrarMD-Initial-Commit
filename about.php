<h3>Meet Dr. Pam</h3>



Dr. Pamila Brar has practiced Internal Medicine in San Diego, California, for fifteen years, including the last three in her own private concierge practice. Her private medicine practice utilizes technology to deliver highly individualized real-time medical care to patients at home, in the office, and across the care continuum. Her "open door style" is appreciated by all, from her adolescent patients up to her centenarians. Pamila is a tireless advocate for her patients, and loves to motivate and empower all under her care. <br />
